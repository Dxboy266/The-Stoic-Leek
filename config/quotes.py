"""
斯多葛哲学语录
"""

import random

STOIC_QUOTES = [
    "你无法控制发生在你身上的事，但你可以控制自己的反应。—— 爱比克泰德",
    "财富不在于拥有多少，而在于需要多少。—— 塞涅卡",
    "困难是检验一个人品格的最好机会。—— 爱比克泰德",
    "真正的幸福是享受现在，而不依赖于未来。—— 塞涅卡",
    "不要为你无法控制的事情烦恼，专注于你能控制的。—— 马可·奥勒留",
    "每一天都是新的生命，智者只活在今天。—— 塞涅卡",
    "障碍即道路，阻力即助力。—— 马可·奥勒留",
]

def get_random_quote() -> str:
    """获取随机语录"""
    return random.choice(STOIC_QUOTES)
